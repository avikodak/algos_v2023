#include "v1/common/Includes.h"

class Solution {
public:
    int addDigits(int num) {
        int sum = 0;
        int temp;
        while (num > 9) {
            sum = 0;
            temp = num;
            while (temp) {
                sum += (temp % 10);
                temp /= 10;
            }
            num = sum;
        }
        return num;
    }
};
